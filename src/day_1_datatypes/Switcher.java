public class Switcher {
	
	public static void main(String[] args) {
		
		switch(5) {
		case 5:
			System.out.println("This is 5");
			break;
		case 6: 
			System.out.println("This is 6");
			break;
		default:
			System.out.println("Not 5 not 6");
		}
		
	}

}
